package classe_abstrata1;

//classes abstratas sao ideias para criarem modelos.

abstract class Pessoa {
    //atributos
    private String nome;
    private String cpf;
    private String rg;
    
    public abstract String retornaDados();
    
    //classes abastratatas tem m√©todos abstratos

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getRg() {
        return rg;
    }

    public void setRg(String rg) {
        this.rg = rg;
    }
}
